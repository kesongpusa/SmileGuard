'use client';

import { useState, useEffect } from 'react';
import { useAuth } from '@smileguard/shared-hooks';
import { bookSlot, getAllBlockedSlots, isSlotTaken, getPatientAppointments } from '@/lib/appointmentService';
import type { Appointment } from '@/lib/database';

const SERVICES = [
  { id: 'cleaning', name: 'Cleaning', duration: 30, price: 1500 },
  { id: 'whitening', name: 'Whitening', duration: 60, price: 5000 },
  { id: 'fillings', name: 'Fillings', duration: 45, price: 2000 },
  { id: 'root-canal', name: 'Root Canal', duration: 90, price: 8000 },
  { id: 'extraction', name: 'Extraction', duration: 30, price: 1500 },
  { id: 'braces', name: 'Braces Consultation', duration: 60, price: 35000 },
  { id: 'implants', name: 'Implants Consultation', duration: 60, price: 45000 },
  { id: 'xray', name: 'X-Ray', duration: 15, price: 500 },
  { id: 'checkup', name: 'Check-up', duration: 20, price: 300 },
];

const TIME_SLOTS = [
  '09:00', '09:30', '10:00', '10:30', '11:00', '11:30',
  '13:00', '13:30', '14:00', '14:30', '15:00', '15:30', '16:00', '16:30',
];

interface BookAppointmentProps {
  onSuccess?: (appointment: Appointment) => void;
  onCancel?: () => void;
}

export default function BookAppointment({ onSuccess, onCancel }: BookAppointmentProps) {
  const { currentUser } = useAuth();
  const [selectedService, setSelectedService] = useState<(typeof SERVICES)[0] | null>(null);
  const [selectedDate, setSelectedDate] = useState<string>('');
  const [selectedTime, setSelectedTime] = useState<string>('');
  const [notes, setNotes] = useState<string>('');
  const [isBooking, setIsBooking] = useState(false);
  const [blockedSlots, setBlockedSlots] = useState<any[]>([]);
  const [loadingBlockedSlots, setLoadingBlockedSlots] = useState(true);
  const [fullyBookedDates, setFullyBookedDates] = useState<Set<string>>(new Set());
  const [userAppointments, setUserAppointments] = useState<Appointment[]>([]);
  const [loadingUserData, setLoadingUserData] = useState(true);

  const step1Complete = selectedService !== null;
  const step2Complete = step1Complete && selectedDate !== '';
  const step3Complete = step2Complete && selectedTime !== '';

  useEffect(() => {
    fetchAllBlockedSlots();
  }, []);

  useEffect(() => {
    if (!currentUser) return;

    async function fetchUserAppointments() {
      setLoadingUserData(true);
      try {
        if (!currentUser) return;
        const appointments = await getPatientAppointments(currentUser.id);
        setUserAppointments(appointments);
      } catch (err) {
        console.error('Error fetching user appointments:', err);
      } finally {
        setLoadingUserData(false);
      }
    }

    fetchUserAppointments();
  }, [currentUser?.id]);

  const fetchAllBlockedSlots = async () => {
    setLoadingBlockedSlots(true);
    try {
      const slots = await getAllBlockedSlots();
      setBlockedSlots(slots);

      const dateCounts: Record<string, number> = {};
      for (const slot of slots) {
        dateCounts[slot.date] = (dateCounts[slot.date] ?? 0) + 1;
      }

      const full = new Set(
        Object.entries(dateCounts)
          .filter(([, count]) => count >= TIME_SLOTS.length)
          .map(([date]) => date)
      );

      setFullyBookedDates(full);
    } catch (error) {
      console.error('Error fetching blocked slots:', error);
      setBlockedSlots([]);
    } finally {
      setLoadingBlockedSlots(false);
    }
  };

  const isSlotDisabled = (date: string, time: string) => {
    return isSlotTaken(blockedSlots, date, time);
  };

  const handleBooking = async () => {
    if (!selectedService || !selectedDate || !selectedTime || !currentUser) {
      alert('Please select service, date, and time');
      return;
    }

    setIsBooking(true);
    try {
      const result = await bookSlot(
        currentUser.id,
        '', // dentistId (optional for now)
        selectedService.name,
        selectedDate,
        selectedTime
      );

      if (result.success) {
        alert('Appointment booked successfully!');
        if (onSuccess) {
          onSuccess({
            id: '1', // This will be generated by Supabase
            patient_id: currentUser.id,
            dentist_id: null,
            service: selectedService.name,
            appointment_date: selectedDate,
            appointment_time: selectedTime,
            notes: notes || '',
            status: 'scheduled',
            created_at: new Date().toISOString(),
          });
        }
        setSelectedService(null);
        setSelectedDate('');
        setSelectedTime('');
        setNotes('');
      }
    } catch (error) {
      console.error('Error booking appointment:', error);
      alert('Failed to book appointment');
    } finally {
      setIsBooking(false);
    }
  };

  if (loadingBlockedSlots) {
    return (
      <div className="flex items-center justify-center min-h-screen">
        <div className="animate-spin rounded-full h-12 w-12 border-b-2 border-brand-cyan"></div>
      </div>
    );
  }

  return (
    <div className="p-6 bg-bg-screen min-h-screen">
      <h1 className="text-4xl font-bold text-brand-cyan mb-2">📅 Book an Appointment</h1>
      <p className="text-text-secondary mb-8">Schedule a visit with our dental professionals</p>

      {/* Upcoming Appointments Summary */}
      {!loadingUserData && userAppointments.length > 0 && (
        <div className="grid grid-cols-1 md:grid-cols-2 gap-4 mb-8">
          {/*
            FIX: bg-green-50 border-green-600 text-green-700 → brand-primary tokens
            Guidelines: green is reserved for "Confirmed/Completed" status badges only.
            Informational summary cards use brand-primary (teal) to stay on-brand.
          */}
          <div className="bg-brand-primary/10 rounded-lg shadow-md p-6 border-l-4 border-brand-primary">
            <p className="text-sm text-text-secondary">Upcoming Appointments</p>
            <p className="text-3xl font-bold text-brand-primary">{userAppointments.length}</p>
            <p className="text-xs text-text-secondary mt-2">Already scheduled</p>
          </div>
          <div className="bg-brand-primary/10 rounded-lg shadow-md p-6 border-l-4 border-brand-primary">
            <p className="text-sm text-text-secondary">Next Available</p>
            <p className="text-3xl font-bold text-brand-primary">{TIME_SLOTS.length} slots</p>
            <p className="text-xs text-text-secondary mt-2">Per day</p>
          </div>
        </div>
      )}

      {/* Booking Form */}
      <div className="bg-bg-surface rounded-lg shadow-md p-6 mb-8 border border-border-card">
        {/* Service Selection */}
        <div className="mb-8">
          <h2 className="text-2xl font-bold text-text-primary mb-4">Step 1: Select Service</h2>
          <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
            {SERVICES.map((service) => (
              <button
                type="button"
                key={service.id}
                onClick={() => setSelectedService(service)}
                className={`p-4 rounded-lg border-2 text-left transition ${
                  selectedService?.id === service.id
                    ? 'border-brand-primary bg-brand-primary/5 shadow-md'
                    : 'border-border-card hover:border-brand-primary/50 hover:shadow'
                }`}
              >
                <div className="font-semibold text-text-primary">{service.name}</div>
                <div className="text-sm text-text-secondary mt-1">{service.duration} mins</div>
                <div className="text-brand-primary font-bold mt-2">₱{service.price}</div>
              </button>
            ))}
          </div>
        </div>

        <hr className="my-8 border-border-card" />

        {/* Date Selection */}
        <div className={`mb-8 transition-opacity ${!step1Complete ? 'opacity-40 pointer-events-none select-none' : ''}`}>
          {!step1Complete && <p className="text-xs text-brand-danger font-medium mb-2">⚠️ Complete Step 1 first</p>}
          <h2 className="text-2xl font-bold text-text-primary mb-4">Step 2: Select Date</h2>
          <input
            type="date"
            value={selectedDate}
            onChange={(e) => setSelectedDate(e.target.value)}
            disabled={fullyBookedDates.has(selectedDate)}
            className="w-full md:w-1/3 p-3 border border-border-card rounded-lg focus:outline-none focus:ring-2 focus:ring-brand-primary bg-bg-surface text-text-primary"
          />
          {fullyBookedDates.has(selectedDate) && (
            <p className="text-sm text-brand-danger mt-2">⚠️ This date is fully booked</p>
          )}
        </div>

        <hr className="my-8 border-border-card" />

        {/* Time Selection */}
        <div className={`mb-8 transition-opacity ${!step2Complete ? 'opacity-40 pointer-events-none select-none' : ''}`}>
          {!step2Complete && <p className="text-xs text-brand-danger font-medium mb-2">⚠️ Complete Step 2 first</p>}
          <h2 className="text-2xl font-bold text-text-primary mb-4">Step 3: Select Time</h2>
          <div className="grid grid-cols-3 md:grid-cols-7 gap-2">
            {TIME_SLOTS.map((time) => (
              <button
                type="button"
                key={time}
                onClick={() => setSelectedTime(time)}
                disabled={isSlotDisabled(selectedDate, time)}
                className={`p-2 rounded-lg text-sm font-medium transition ${
                  selectedTime === time
                    ? 'bg-brand-primary text-text-on-avatar shadow-md'
                    : isSlotDisabled(selectedDate, time)
                      ? 'bg-border-card text-text-secondary cursor-not-allowed'
                      : 'bg-brand-primary/10 hover:bg-brand-primary/20 hover:border-brand-primary/50'
                }`}
              >
                {time}
              </button>
            ))}
          </div>
        </div>

        <hr className="my-8 border-border-card" />

        {/* Notes */}
        <div className={`mb-8 transition-opacity ${!step3Complete ? 'opacity-40 pointer-events-none select-none' : ''}`}>
          {!step3Complete && <p className="text-xs text-brand-danger font-medium mb-2">⚠️ Complete Step 3 first</p>}
          <h2 className="text-2xl font-bold text-text-primary mb-4">Step 4: Additional Notes (Optional)</h2>
          <textarea
            value={notes}
            onChange={(e) => setNotes(e.target.value)}
            placeholder="Any special requests, medical concerns, or conditions we should know about..."
            className="w-full p-4 border border-border-card rounded-lg focus:outline-none focus:ring-2 focus:ring-brand-primary bg-bg-notes text-text-primary"
            rows={4}
          />
        </div>
      </div>

      {/* Action Buttons */}
      <div className="flex flex-col md:flex-row gap-4">
        <button
          type="button"
          onClick={handleBooking}
          disabled={isBooking || !step3Complete}
          className={`flex-1 p-4 font-bold rounded-pill transition text-lg text-text-on-danger ${
            step3Complete
              ? 'bg-brand-primary hover:bg-brand-primary/90'
              : 'bg-border-card cursor-not-allowed text-text-secondary'
          }`}
        >
          {isBooking ? '⏳ Booking...' : step3Complete ? '✓ Confirm Appointment' : '⬆ Complete all steps to confirm'}
        </button>
        {onCancel && (
          <button
            type="button"
            onClick={onCancel}
            className="flex-1 p-4 bg-border-card text-text-primary font-semibold rounded-pill hover:bg-border-card/80 transition"
          >
            Cancel
          </button>
        )}
      </div>
    </div>
  );
}
